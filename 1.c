#include<stdio.h>
int main()
{
	int a, b, c, d;
	printf("Enter the number:");
	scanf("%d", &a);
	
	printf("Enter the second number:");
	scanf("%d", &b);
	
	printf("Enter the third number:");
	scanf("%d", &c);
	
	printf("Enter the fourth number:");
	scanf("%d", &d);
	
	printf("Sum is %d\n", a+b+c+d);
	printf("Subtract is %d\n", a-b-c-d);
	printf("Subtract is %d\n", a/b/c/d);
	printf("Subtract is %d\n", a*b*c*d);
	return 0;
}
